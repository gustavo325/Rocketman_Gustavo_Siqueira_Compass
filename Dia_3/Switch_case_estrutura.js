// Switch.. Case

let permissao; // comum, gerente, diretor

switch(permissao) {
    case 'comum':
    console.log('usuário comum'); 
    break
    case 'gerente': 
    console.log("usuário gerente"); 
    break
    case 'diretor':
    console.log('diretor'); 
    break
    default: // Caso não seja nenhum dos três; 
    console.log('usuário não reconhecido!')
}

